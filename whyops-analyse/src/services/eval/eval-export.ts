import { createServiceLogger } from '@whyops/shared/logger';
import { EvalCase } from '@whyops/shared/models';

const logger = createServiceLogger('analyse:eval:export');

// ---------------------------------------------------------------------------
// JSON Export
// ---------------------------------------------------------------------------
export interface JsonExportCase {
  id: string;
  category: string;
  subcategory: string | null;
  title: string;
  description: string | null;
  difficulty: string;
  conversation: any[];
  expected_outcome: any;
  scoring_rubric: any;
  tools_tested: string[];
}

export async function exportAsJson(agentId: string, runId?: string): Promise<JsonExportCase[]> {
  const where: Record<string, any> = { agentId };
  if (runId) where.runId = runId;

  const cases = await EvalCase.findAll({
    where,
    order: [['category', 'ASC'], ['createdAt', 'ASC']],
  });

  logger.info({ agentId, runId, count: cases.length }, 'Exporting evals as JSON');

  return cases.map((c) => ({
    id: c.id,
    category: c.category,
    subcategory: c.subcategory || null,
    title: c.title,
    description: c.description || null,
    difficulty: c.difficulty,
    conversation: c.conversation,
    expected_outcome: c.expectedOutcome,
    scoring_rubric: c.scoringRubric,
    tools_tested: c.toolsTested,
  }));
}

// ---------------------------------------------------------------------------
// Promptfoo YAML Export
// ---------------------------------------------------------------------------
interface PromptfooTest {
  description: string;
  vars: Record<string, any>;
  assert: Array<{
    type: string;
    value?: string;
    metric?: string;
  }>;
}

interface PromptfooConfig {
  description: string;
  prompts: string[];
  providers: Array<{ id: string; config?: Record<string, any> }>;
  tests: PromptfooTest[];
}

function buildPromptfooAssertions(evalCase: EvalCase): PromptfooTest['assert'] {
  const assertions: PromptfooTest['assert'] = [];
  const outcome = evalCase.expectedOutcome as any;
  const rubric = evalCase.scoringRubric as any;

  // LLM rubric from scoring dimensions
  if (rubric?.dimensions && Array.isArray(rubric.dimensions)) {
    const rubricText = rubric.dimensions
      .map((d: any) => `${d.name} (weight: ${d.weight}): ${d.criteria}`)
      .join('\n');

    assertions.push({
      type: 'llm-rubric',
      value: rubricText,
    });
  }

  // Key assertions as contains checks
  if (outcome?.key_assertions && Array.isArray(outcome.key_assertions)) {
    for (const assertion of outcome.key_assertions) {
      assertions.push({
        type: 'llm-rubric',
        value: assertion,
        metric: 'key_assertion',
      });
    }
  }

  // Refusal check
  if (outcome?.refusal_expected) {
    assertions.push({
      type: 'llm-rubric',
      value: 'The response should refuse or decline the request appropriately, explaining why it cannot fulfill it.',
      metric: 'refusal',
    });
  }

  return assertions;
}

export async function exportAsPromptfoo(
  agentId: string,
  agentName: string,
  systemPrompt: string,
  runId?: string
): Promise<string> {
  const where: Record<string, any> = { agentId };
  if (runId) where.runId = runId;

  const cases = await EvalCase.findAll({
    where,
    order: [['category', 'ASC'], ['createdAt', 'ASC']],
  });

  logger.info({ agentId, runId, count: cases.length }, 'Exporting evals as Promptfoo YAML');

  const config: PromptfooConfig = {
    description: `Eval suite for ${agentName} — generated by WhyOps`,
    prompts: [systemPrompt || 'You are a helpful assistant.'],
    providers: [
      {
        id: 'openai:gpt-4',
        config: {
          temperature: 0,
        },
      },
    ],
    tests: cases.map((c) => {
      // For multi-turn, use the first user message as the primary input
      const conversation = c.conversation as any[];
      const firstUserTurn = conversation.find((t: any) => t.role === 'user');

      return {
        description: `[${c.category}${c.subcategory ? '/' + c.subcategory : ''}] ${c.title}`,
        vars: {
          query: firstUserTurn?.content || c.title,
          category: c.category,
          difficulty: c.difficulty,
          ...(conversation.length > 2 ? { full_conversation: JSON.stringify(conversation) } : {}),
        },
        assert: buildPromptfooAssertions(c),
      };
    }),
  };

  // Convert to YAML manually (avoids adding a yaml dependency)
  return toYaml(config);
}

// ---------------------------------------------------------------------------
// Minimal YAML serializer (avoids dependency)
// ---------------------------------------------------------------------------
function toYaml(obj: any, indent = 0): string {
  const pad = '  '.repeat(indent);

  if (obj === null || obj === undefined) return `${pad}null\n`;
  if (typeof obj === 'boolean') return `${pad}${obj}\n`;
  if (typeof obj === 'number') return `${pad}${obj}\n`;

  if (typeof obj === 'string') {
    if (obj.includes('\n') || obj.includes(':') || obj.includes('#') || obj.includes('"')) {
      return `${pad}|\n${obj
        .split('\n')
        .map((line) => `${'  '.repeat(indent + 1)}${line}`)
        .join('\n')}\n`;
    }
    return `${pad}"${obj.replace(/"/g, '\\"')}"\n`;
  }

  if (Array.isArray(obj)) {
    if (obj.length === 0) return `${pad}[]\n`;
    return obj
      .map((item) => {
        if (typeof item === 'object' && item !== null) {
          const entries = Object.entries(item);
          const firstEntry = entries[0];
          const rest = entries.slice(1);

          let result = `${pad}- ${firstEntry[0]}: ${formatInlineValue(firstEntry[1])}\n`;
          for (const [key, val] of rest) {
            if (typeof val === 'object' && val !== null) {
              result += `${pad}  ${key}:\n${toYaml(val, indent + 2)}`;
            } else {
              result += `${pad}  ${key}: ${formatInlineValue(val)}\n`;
            }
          }
          return result;
        }
        return `${pad}- ${formatInlineValue(item)}\n`;
      })
      .join('');
  }

  if (typeof obj === 'object') {
    return Object.entries(obj)
      .map(([key, val]) => {
        if (typeof val === 'object' && val !== null) {
          return `${pad}${key}:\n${toYaml(val, indent + 1)}`;
        }
        return `${pad}${key}: ${formatInlineValue(val)}\n`;
      })
      .join('');
  }

  return `${pad}${String(obj)}\n`;
}

function formatInlineValue(val: any): string {
  if (val === null || val === undefined) return 'null';
  if (typeof val === 'boolean' || typeof val === 'number') return String(val);
  if (typeof val === 'string') {
    if (val.includes('\n') || val.includes('"')) {
      return `"${val.replace(/"/g, '\\"').replace(/\n/g, '\\n')}"`;
    }
    return `"${val}"`;
  }
  return JSON.stringify(val);
}
